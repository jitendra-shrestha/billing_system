    private void display_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_display_userMouseClicked
            int i = display_user.getSelectedRow();
            TableModel model = display_user.getModel();
            String suid = model.getValueAt(i, 0).toString();
            jPanel1.setVisible(false);
            jPanel2.setVisible(true);
            jtxtfirstname1.setText(model.getValueAt(i, 1).toString());
            jtxtlastname1.setText(model.getValueAt(i, 2).toString());
            jtxtaddress1.setText(model.getValueAt(i, 3).toString());
            jtxtemail1.setText(model.getValueAt(i, 4).toString());
            jtxtphoneno1.setText(model.getValueAt(i, 5).toString());
            
            String type=model.getValueAt(i, 6).toString();
            switch(type){
                case"admin":
                    jComboBoxtype1.setSelectedIndex(0);
                    break;
                case"cashier":
                    jComboBoxtype1.setSelectedIndex(1);
                    break;
            }
            jtxtusername1.setText(model.getValueAt(i, 7).toString());
            String designation=model.getValueAt(i, 8).toString();
            switch(designation){
                case"manager":
                    jComboBoxdesignation1.setSelectedIndex(0);
                    break;
                case"cashier":
                    jComboBoxdesignation1.setSelectedIndex(1);
                    break;
            }
            String shift=model.getValueAt(i, 9).toString();
            switch(shift){
                case"morning":
                    jComboBoxshift1.setSelectedIndex(0);
                    break;
                case"day":
                    jComboBoxshift1.setSelectedIndex(1);
                    break;
                case"evening":
                    jComboBoxshift1.setSelectedIndex(2);
                    break;
            }
            
            String status=model.getValueAt(i, 10).toString();
            if(status.equals("1")){
                statusvar.setText("1");
                jToggleButton1.setSelected(true);
                jToggleButton1.setBackground(Color.GREEN);
                jToggleButton1.setText("Active");
            }else{
                statusvar.setText("0");
                jToggleButton1.setSelected(false);
                jToggleButton1.setBackground(Color.RED);
                jToggleButton1.setText("InActive");
            }
            
    }//GEN-LAST:event_display_userMouseClicked

