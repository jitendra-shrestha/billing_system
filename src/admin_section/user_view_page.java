    public ArrayList<User> userlist(){
        ArrayList<User> userslist= new ArrayList<>();
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/market","root","");
            Statement st= con.createStatement();
           
            ResultSet rs;
            String query = "SELECT * FROM users";
            rs = st.executeQuery(query);
            User user;
            while(rs.next()){
                user=new User(rs.getInt("u_id"),rs.getString("firstname"),rs.getString("lastname"),rs.getString("address"),
                rs.getString("email"),rs.getString("phoneno"),rs.getString("type"),rs.getString("username"),rs.getString("designation"),
                rs.getString("shift"),rs.getInt("status"));
                userslist.add(user);
            }
            
         st.close();
        con.close();
         }
          catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
         return userslist;
    }
    
